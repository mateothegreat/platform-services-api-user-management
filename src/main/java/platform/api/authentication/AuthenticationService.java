import platform.services.api.users.entities.User;
import platform.services.api.users.services.UserService;
import org.apache.logging.log4j.LogManager;
import org.apache.logging.log4j.Logger;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;

import java.util.ArrayList;
import java.util.List;

@Service
public class AuthenticationService implements UserDetailsService {
    
    private static final Logger log = LogManager.getLogger(AuthenticationService.class);
    
    @Autowired
    private UserService service;
    
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        
        log.trace("loadUserByUsername: {}", username);
        
        User user = service.getUserByUsername(username);
        
        if(user == null) {
            
            throw new UsernameNotFoundException(username);
            
        }
        
        List<GrantedAuthority> grantedAuthorities = new ArrayList<>();
        List<String>           permissions        = service.getPermissions(user.getUsername());
        
        log.trace("loadUserByUsername->service.getPermissions({}): {}", user.getUsername(), permissions.toString());
        
        for(String permission : permissions) {
            
            log.trace("loadUserByUsername->grantedAuthorities.add: {}", permission);
            
            grantedAuthorities.add(new SimpleGrantedAuthority(permission));
            
        }
        
        return new CustomUserDetails(user, grantedAuthorities);
        
    }
    
}
